#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t len)
{
	unsigned char	*cdst;
	unsigned char	*csrc;

	cdst = (unsigned char *)dst;
	csrc = (unsigned char *)src;
	if (!len || dst == src)
		return (dst);
	while (len--)
		*cdst++ = *csrc++;
	return (dst);
}
